package unit;

public class DatDichVu {
	private String maDdv;
	private String maKh;
	private String maHd;
	private String maDv;
	public DatDichVu(String maDdv, String maKh,String maHd, String maDv) {
		this.maDdv = maDdv;
		this.maKh = maKh;
		this.maHd = maHd;
		this.maDv = maDv;
	}
	public String getMaDdv() {
		return maDdv;
	}
	public void setMaDdv(String maDdv) {
		this.maDdv = maDdv;
	}
	public String getMaKh() {
		return maKh;
	}
	public void setMaKh(String maKh) {
		this.maKh = maKh;
	}
	public String getMaHd() {
		return maHd;
	}
	public void setMaHd(String maHd) {
		this.maHd = maHd;
	}
	public String getMaDv() {
		return maDv;
	}
	public void setMaDv(String maDv) {
		this.maDv = maDv;
	}

}
